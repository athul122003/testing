// Enforce strict filtering: unique + length === 5
const WORDS = Array.from(
	new Set(
		WINTER_WORDS.map((w) => w.toLowerCase()).filter((w) => w.length === 5),
	),
);

export async function seedWords() {
	console.log(`Seeding ${WORDS.length} winter Wordle words...`);

	for (const word of WORDS) {
		await db.wordleWord.upsert({
			where: { word },
			update: {},
			create: {
				word,
				length: 5,
			},
		});
	}

	console.log("Wordle winter words seeded successfully");
}
